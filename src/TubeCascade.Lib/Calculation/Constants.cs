namespace TubeCascade.Calculation;

/// <summary>
/// Metric system's decimal unit prefixes.
/// </summary>
internal static class Constants
{
	/// <summary>
	/// Milli - 0.001
	/// </summary>
	public const double Milli = 0.001;
	
	/// <summary>
	/// Kilo - 1000.
	/// </summary>
	public const double Kilo = 1000;

	/// <summary>
	/// Mega - 1 000 000.
	/// </summary>
	public const double Mega = 1000 * Kilo;
}